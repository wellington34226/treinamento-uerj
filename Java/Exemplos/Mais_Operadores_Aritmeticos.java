
public class Mais_Operadores_Aritmeticos {
  public static void main (String[] args) {

    int result = +1;        
    System.out.println(result);

    result--;        
    System.out.println(result);

    result++;        
    System.out.println(result);

    result = -result;        
    System.out.println(result);

    boolean success = false;        
    System.out.println(success);       
    System.out.println(!success);
    
    int i = 3;
    i++;       
    System.out.println(i);
    ++i;	
    System.out.println(i);       
    System.out.println(++i);        
    System.out.println(i++);        
    System.out.println(i);
  }
}
